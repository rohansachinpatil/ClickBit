"""
ui/theme/spacing.py
-------------------
Unified strict layout spacing constants for paddings, gaps, and margins.
"""

class Spacing:
    XS = 4   # Micro margins, compact chip paddings
    SM = 8   # Standard layout gap, button internals
    MD = 12  # Cards padding, standard internal layout margin
    LG = 20  # Outer layout paddings, frame bounds
    XL = 32  # Large margins, spacing between heavy sections
